// FILE: generate-web-asset-map.js
// Purpose: Bundles the /web PWA shell into a static asset map for Cloudflare Worker deploys.
// Layer: Build helper
// Exports: generateWebAssetMap, resolveExtraAssets
// Depends on: fs, path, ./web-asset-pipeline

const fs = require("fs");
const path = require("path");

const {
  WEB_ROOT,
  buildWebAssetRecords,
  buildWebAssetVersion,
  resolveExtraAssets,
  resolveWebAssetEntries,
} = require("./web-asset-pipeline");

const DEFAULT_OUTPUT_FILE = path.resolve(__dirname, "..", "cloudflare", "web-assets.generated.mjs");

function generateWebAssetMap({
  webRoot = WEB_ROOT,
  outputFile = DEFAULT_OUTPUT_FILE,
} = {}) {
  const assetEntries = resolveWebAssetEntries({ webRoot });
  const version = buildWebAssetVersion({ assetEntries });
  const assets = buildWebAssetRecords({
    assetEntries,
    version,
  });

  const output = [
    "// FILE: web-assets.generated.mjs",
    "// Purpose: Generated static asset map for serving /app/ from Cloudflare Worker.",
    "// Generated by: src/generate-web-asset-map.js",
    "",
    `export const WEB_ASSET_VERSION = ${JSON.stringify(version)};`,
    "",
    "export const WEB_ASSETS = new Map(",
    `  ${JSON.stringify(assets, null, 2)}.map((entry) => [entry.path, entry])`,
    ");",
    "",
  ].join("\n");

  fs.writeFileSync(outputFile, output, "utf8");
  return outputFile;
}

if (require.main === module) {
  const outputFile = generateWebAssetMap();
  console.log(`[remodex] Generated Cloudflare web asset map at ${outputFile}`);
}

module.exports = {
  generateWebAssetMap,
  resolveExtraAssets,
};
